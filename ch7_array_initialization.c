#include <stdio.h>

int main()
{
    int arr1[3] = {10, 15, 20};
    float arr2[] = {7.5, 14.5, 21.5};
    char arr3[6] = {'M', 'R', 'I', 'N', 'A', 'L'};
    printf("The value of arr1[0] is %d \n", arr1[0]);
    printf("The value of arr1[1] is %d \n", arr1[1]);
    printf("The value of arr1[2] is %d \n", arr1[2]);

    printf("The value of arr2[0] is %f \n", arr2[0]);
    printf("The value of arr2[1] is %f \n", arr2[1]);
    printf("The value of arr2[2] is %f \n", arr2[2]);

    printf("The value of arr3[0] is %c \n", arr3[0]);
    printf("The value of arr3[1] is %c \n", arr3[1]);
    printf("The value of arr3[2] is %c \n", arr3[2]);
    printf("The value of arr3[3] is %c \n", arr3[3]);
    printf("The value of arr3[4] is %c \n", arr3[4]);
    printf("The value of arr3[5] is %c \n", arr3[5]);
    return 0;
}