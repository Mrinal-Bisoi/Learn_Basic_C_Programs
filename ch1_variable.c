#include <stdio.h>

int main()
{
    int a = 7;
    float b = 8.5;
    char c = 'M';
    int d = 8;
    int e = 12 + 3;

    printf("The value of a is %d.  \n", a);
    printf("The value of b is %0.1f.  \n", b);
    printf("The value of c is %c.  \n", c);
    printf("The sum of a & d is %d.  \n", a + d);
    printf("The value of e is %d.  \n", e);

    return 0;
}